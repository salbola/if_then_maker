class ThenActionWarningChecker
WARNING_KEYWORDS = {
  # 抽象的で完了判定ができない行動（表記ゆれは同一扱い）
  "考える"   => "完了したかどうかを判断できない抽象的な行動です。",
  "かんがえる" => "完了したかどうかを判断できない抽象的な行動です。",

  "意識する"   => "行動として観測しづらく、完了判定ができない表現です。",
  "いしきする" => "行動として観測しづらく、完了判定ができない表現です。",

  "頑張る"   => "努力量に依存しており、完了条件が不明確な行動です。",
  "がんばる" => "努力量に依存しており、完了条件が不明確な行動です。",
  "ガンバる" => "努力量に依存しており、完了条件が不明確な行動です。",

  "気をつける"   => "心構えに近く、具体的な行動として判定できません。",
  "きをつける"   => "心構えに近く、具体的な行動として判定できません。",
  "気を付ける"   => "心構えに近く、具体的な行動として判定できません。",

  "やる" => "行動内容が抽象的で、具体的な実行イメージが持ちにくい表現です。",
  "する" => "行動内容が抽象的で、具体的な実行イメージが持ちにくい表現です。",

  # 行動が大きくなりやすい表現（分割推奨）
  "終わらせる"   => "行動の範囲が広くなりやすい表現です。最初の一歩に分解すると実行しやすくなります。",
  "おわらせる"   => "行動の範囲が広くなりやすい表現です。最初の一歩に分解すると実行しやすくなります。",
  "完了させる"   => "行動の範囲が広くなりやすい表現です。最初の一歩に分解すると実行しやすくなります。",
  "かんりょうさせる" => "行動の範囲が広くなりやすい表現です。最初の一歩に分解すると実行しやすくなります。",

  "完璧にする"   => "完了条件が厳しくなりやすい表現です。小さな行動に分けることをおすすめします。",
  "かんぺきにする" => "完了条件が厳しくなりやすい表現です。小さな行動に分けることをおすすめします。",
  "パーフェクトにする" => "完了条件が厳しくなりやすい表現です。小さな行動に分けることをおすすめします。"
}.freeze

  def self.check(check_text)
    return [] if check_text.blank?

    WARNING_KEYWORDS.filter_map do |keyword, message|
      message if check_text.include?(keyword)
    end
  end
end